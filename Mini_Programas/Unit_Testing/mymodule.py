"""
Modulo para realizar multiples operaciones matematicas
"""
def square(number):
    """
    This function returns the square of a given number
    """
    return number ** 2

def double(number):
    """
    This function return  twice the value of a given number
    """
    return number * 2

def add(number1, number2):
    '''Funcion para sumar 2 numeracos'''

    return number1 + number2

